#include "3-calc.h"

/**
 *op_add - adds the two ints
 *
 * @a: first int
 * @b: second int
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - subtracts two ints
 *
 * @a: first int
 * @b: second string
 *
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - calculates products
 *
 *@a: first int
 *@b: second int
 *
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divides two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: Returns quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - calculates remainder
 *
 * @a: first int
 * @b: second int
 *
 * Return: Returns quotient
 */
int op_mod(int a, int b)
{
	return (a % b);
}
